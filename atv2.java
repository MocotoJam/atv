//atribuições entre variáveis
public class atv2 {
    public static void main(String[] args) {
        //inicializando as variaveis com os valores iniciais
        int a = 1;
        int b = 2;
        int c = 3;
        System.out.println("-----------------");
        System.out.println("Valor original: \n"+"a:"+a+" b:"+b+" c:"+c);
        
        //mudando o valor inicial das variaveis
        a = 3;
        b = 1;
        c = 2;
        System.out.println("-----------------");
        System.out.println("Valor atualizado: \n"+"a:"+a+" b:"+b+" c:"+c);
        System.out.println("-----------------");
    }
    
}
